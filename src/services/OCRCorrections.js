// Apply corrections word by word to a pasted inscription
export function correctOCR(text) {
  if (!text) return text
  return text
    .toUpperCase()
    .split(/\s+/)
    .map(word => OCR_CORRECTIONS[word] || word)
    .join(' ')
    .trim()
}
